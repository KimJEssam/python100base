print (a*b)     # 10*3 = 30     곱셈
print (a/b)     # 10/3 = 3.333..나눗셈
print (a%b)    # 10%3 = 1      mod(나머지)
print (a//b)    # 10//3 = 3     몫
print (a**b)    # 10**3 = 1000  거듭제곱
